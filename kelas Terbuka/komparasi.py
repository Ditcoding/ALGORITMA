a = 5
b = 4

print("========================Kurang dari <")
nilai = a < 3
print(a, "<", 3,nilai )
nilai = b < 3
print(b, "<", 3 ,nilai )

print("========================Lebih dari")
nilai = a > 3
print(a, ">", 3, nilai)
nilai = b > 3
print(b, ">", 3, nilai)

print("========================kurang dari samadengan")
nilai = a >= 5
print(a, ">=", 5, nilai)
nilai = b >= 3
print(b, ">=", 3, nilai)

print("========================Lebih dari samadengan")
nilai = a <= 3
print(a, "<=",3 , nilai)
nilai = b <= 4
print(b, "<=",4 , nilai)

print("========================sama dengan")
nilai = a == 5
print(a, "==", 5, nilai)
nilai = b == 3
print(b, "==", 3, nilai)

print("========================Tidak sama dengan")
nilai = a != 5
print(a, "!=", 5, nilai)
nilai = b != 3
print(b, "!=", 3, nilai)

